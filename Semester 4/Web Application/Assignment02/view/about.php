<p>
The GameStop Corporation (formerly EB Games) is an American video game and entertainment software retailer. The company, whose headquarters is in 			Grapevine, Texas, United States, operates 6,700 retail stores throughout the United States, Canada, Australia, Austria, Denmark, Finland, France, Germany, Ireland, Italy, New Zealand, Norway, Portugal, Puerto Rico, Spain, Sweden, and Switzerland. Both of the two stores in the United Kingdom failed to compete with existing Game stores, and were closed in 2011. GameStop launched a UK website the same day. GameStop opened around 400 new stores for the 2009 business year.
</p>

